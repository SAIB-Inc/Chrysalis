        private static StringBuilder EmitMaybeIndefListPartialProperty(StringBuilder sb, SerializablePropertyMetadata prop, string fieldRef, string fqType)
        {
            string itemFqn = prop.CborMaybeIndefListItemTypeFullName!;
            string cleanItemFqn = itemFqn.Replace("?", "");
            string nonNullType = fqType.Replace("?", "");

            _ = sb.AppendLine($"public partial {fqType} {prop.PropertyName}");
            _ = sb.AppendLine("{");
            _ = sb.AppendLine("get");
            _ = sb.AppendLine("{");

            if (prop.IsTypeNullable)
            {
                _ = sb.AppendLine($"if ({fieldRef}.Length == 0 || {fieldRef}.Span[0] is 0xF6 or 0xF7) return default;");
            }

            // Emit cache registration for the item type
            string? primitiveReaderRef = GetPrimitiveReaderRef(cleanItemFqn);
            if (primitiveReaderRef != null)
            {
                _ = sb.AppendLine($"global::Chrysalis.Codec.Serialization.CborListReaderCache<{itemFqn}>.Reader ??= {primitiveReaderRef};");
            }
            else
            {
                _ = sb.AppendLine($"global::Chrysalis.Codec.Serialization.CborListReaderCache<{itemFqn}>.Reader ??= {cleanItemFqn}.Read;");
            }

            _ = sb.AppendLine($"return {nonNullType}.Read({fieldRef});");
            _ = sb.AppendLine("}");
            _ = sb.AppendLine("}");

            return sb;
        }

        private static string? GetPrimitiveReaderRef(string cleanType)
        {
            const string prefix = "global::Chrysalis.Codec.Serialization.CborPrimitiveReaders";
            return cleanType switch
            {
                "int" => $"{prefix}.ReadInt32",
                "uint" => $"{prefix}.ReadUInt32",
                "long" => $"{prefix}.ReadInt64",
                "ulong" => $"{prefix}.ReadUInt64",
                "bool" => $"{prefix}.ReadBoolean",
                "string" => $"{prefix}.ReadString",
                "float" => $"{prefix}.ReadSingle",
                "double" => $"{prefix}.ReadDouble",
                "byte[]" => $"{prefix}.ReadByteArray",
                "ReadOnlyMemory<byte>" or "System.ReadOnlyMemory<byte>" or "global::System.ReadOnlyMemory<byte>"
                    => $"{prefix}.ReadByteMemory",
                _ => null
            };
        }

        private static ICborSerializerEmitter GetEmitter(SerializableTypeMetadata metadata) => metadata.SerializationType switch
        {
            SerializationType.Constr => new ConstructorEmitter(),
            SerializationType.Container => new ContainerEmitter(),
            SerializationType.List => new ListEmitter(),
            SerializationType.Map => new MapEmitter(),
            SerializationType.Union => new UnionEmitter(),
            _ => throw new NotSupportedException($"Serialization type {metadata.SerializationType} is not supported.")
        };

        public static int ResolveTag(int? index)
        {
            if (index is null or < 0)
            {
                return -1;
            }
            else
            {
                int finalIndex = index > 6 ? 1280 - 7 : 121;
                return finalIndex + index.Value;
            }
        }

